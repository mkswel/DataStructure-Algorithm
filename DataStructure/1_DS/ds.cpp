/**
 * 特点：
 * 1）有穷性：一个算法必须有限步骤后结束，死循环不是算法，属于程序
 * 2）确定性：相同的输入得到相同的输出
 * 3）可行性：算法可以通过已实现的基本运算执行有限次来实现
 * 4）输入：0个或多个输入
 * 5）输出：1个或多个输出
 * 
 * 目标：
 * 1）正确性：正确求解问题
 * 2）可读性：可读，易于理解
 * 3）健壮性：对于非法输入有准备措施
 * 4）高效率和低存储量需求
 */ 